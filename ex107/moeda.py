def aumentar(numero, porcentagem):
    resultuado = numero*(1 + porcentagem/100)
    return resultuado

def diminuir(numero, porcentagem):
    resultado = numero*(1 - porcentagem/100)
    return resultado

def dobro(numero):
    resultado = numero*2
    return resultado

def metade(numero):
    resultado = numero/2
    return resultado